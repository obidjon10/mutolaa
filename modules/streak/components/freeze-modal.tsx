export function FreezeModal({
  isOpen,
  onClose,
  packages,
  initialPackage,
  userCoins,
}: IProps) {
  const t = useTranslations();
  const [step, setStep] = useState<StepType>("purchase");
  const [selected, setSelected] = useState<ICoinsPackage | null>(null);
  const { mutate: purchase, isPending } = usePurchaseFreeze();

  // Derive the effective selection: explicit user pick → clicked package → first package
  const effectiveSelected = selected ?? initialPackage ?? packages[0] ?? null;
  const hasEnoughCoins = (effectiveSelected?.coin_cost ?? 0) <= (userCoins || 0);

  const handlePurchase = () => {
    if (!effectiveSelected) return;
    purchase(effectiveSelected.id, {
      onSuccess: () => setStep("success"),
      onError: () => setStep("error"),
    });
  };

  return (
    <Modal.Root
      isOpen={isOpen}
      onOpenChange={(open) => {
        if (!open) {
          // Reset for next open — safe here because this is an event callback, not an effect
          setStep("purchase");
          setSelected(null);
          onClose();
        }
      }}
    >
      <Modal.Backdrop>
        <Modal.Container>
          <Modal.Dialog
            className={classNames("sm:max-w-110 p-4", {
              "bg-linear-to-b from-5% to-95% via-white from-[#C2ECFD] to-white dark:to-black dark:via-black":
                step === "purchase",
            })}
          >
            {isOpen && (
              <div className="relative text-center">
                <Modal.CloseTrigger className="absolute top-0 right-0 flex size-7 items-center justify-center rounded-full bg-white dark:bg-black text-foreground-muted hover:bg-gray-200 dark:hover:bg-[#3F3F46]">
                  <XMarkIcon size={16} />
                </Modal.CloseTrigger>

                {step === "purchase" && (
                  <>
                    <div className="size-28 mx-auto mb-8 mt-3 rounded-full relative flex items-center justify-center">
                      <Image
                        fill
                        sizes="140px"
                        src="/freeze.webp"
                        alt="Freeze"
                      />
                    </div>

                    <h2 className="text-2xl font-bold">
                      {effectiveSelected && hasEnoughCoins
                        ? `${effectiveSelected.freeze_count} ta muz — ${effectiveSelected.coin_cost} bilig`
                        : t("biliglar_ozlik_qilyapti")}
                    </h2>

                    <p
                      className={classNames(
                        "text-sm text-foreground-muted mx-auto mt-2",
                        {
                          "w-60": !hasEnoughCoins,
                          "w-70": hasEnoughCoins,
                        },
                      )}
                    >
                      {hasEnoughCoins
                        ? t("muz_xarid_tavsifi")
                        : t("muz_sotib_olish_uchun_tolayin")}
                    </p>

                    <Separator className="my-4 w-70 mx-auto" />

                    <div className="inline-flex items-center gap-1 p-3 border border-[#00000014] rounded-full text-xs font-medium">
                      <span className="text-[#696969]">
                        {t("mavjud_biliglaringiz")}
                      </span>{" "}
                      — <BiligCoinIcon size={12} />
                      {userCoins || 0}
                    </div>

                    <div className="flex gap-3 mt-11">
                      <ConditionalRender if={hasEnoughCoins}>
                        <Button
                          variant="tertiary"
                          className="flex-1"
                          onPress={onClose}
                        >
                          {t("hozir_emas")}
                        </Button>
                      </ConditionalRender>
                      <Button
                        className="flex-1 bg-brand text-white"
                        isDisabled={!hasEnoughCoins}
                        isPending={isPending}
                        onPress={handlePurchase}
                      >
                        {t("sotib_olish")}
                      </Button>
                    </div>
                  </>
                )}

                {step === "success" && (
                  <>
                    <div className="flex items-center justify-center mt-2">
                      <SuccessIcon />
                    </div>
                    <h2 className="text-2xl font-semibold mt-8">
                      {t("muzdek_xotirjamlik")}
                    </h2>
                    <p className="text-sm mt-1 mb-11">
                      {t("muzdek_xotirjamlik_tavsifi")}
                    </p>
                    <Button
                      className="w-full bg-brand text-white"
                      onPress={onClose}
                    >
                      {t("tushunarli")}
                    </Button>
                  </>
                )}

                {step === "error" && (
                  <>
                    <div className="flex items-center justify-center mt-2">
                      <ErrorIcon />
                    </div>
                    <div className="w-85 mx-auto mb-11">
                      <h2 className="text-2xl font-semibold px-5 mt-8">
                        {t("muz_xarid_xatolik")}
                      </h2>
                      <p className="text-sm mt-1">
                        {t("muz_xarid_xatolik_tavsifi")}
                      </p>
                    </div>
                    <Button
                      className="w-full bg-brand text-white"
                      onPress={() => setStep("purchase")}
                    >
                      {t("qayta_urinish")}
                    </Button>
                  </>
                )}
              </div>
            )}
          </Modal.Dialog>
        </Modal.Container>
      </Modal.Backdrop>
    </Modal.Root>
  );
}
